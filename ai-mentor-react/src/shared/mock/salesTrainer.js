/**
 * Seed the sales trainer into localStorage if not already present.
 */
export function seedSalesTrainer() {
  const KEY = 'ai-mentor-builder-data-v1'
  try {
    const stored = JSON.parse(localStorage.getItem(KEY) || '{}')
    if (!stored[SALES_TRAINER_ID]) {
      stored[SALES_TRAINER_ID] = SALES_TRAINER_UNIT
      localStorage.setItem(KEY, JSON.stringify(stored))
    }
  } catch (e) {
    console.warn('seedSalesTrainer:', e)
  }
}
